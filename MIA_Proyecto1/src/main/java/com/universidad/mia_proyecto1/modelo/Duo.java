package com.universidad.mia_proyecto1.modelo;

public class Duo {
    String primero;
    String segundo;

    public Duo(String primero, String segundo){
        this.primero = primero;
        this.segundo = segundo;
    }
    
    public String getPrimero() {
        return primero;
    }
    public String getSegundo() {
        return segundo;
    }
}
